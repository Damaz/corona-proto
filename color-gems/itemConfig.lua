function getItem(name, level)
  printMsg("getItem : " .. name .. ", " .. level)
  if (level == 1) then
    return firstLevelTable[name]
  elseif (level == 2) then
    return secondLevelTable[name]
  elseif (level == 3) then
    return thirdLevelTable[name]
  elseif (level == 4) then
    return fourthLevelTable[name]
  elseif (level == 5) then
    return fifthLevelTable[name]
  elseif (level == 6) then
    return sixLevelTable[name]
  else
    return nil
  end
end